"""#**Exercise 6: Reverse a Number**

---


"""

#reversing a number

num = int(input("Enter a number: "))
reversed_num = 0

while num > 0:
    remainder = num % 10
    reversed_num = reversed_num * 10 + remainder
    num = num // 10

print(f"The reversed number is {reversed_num}")

